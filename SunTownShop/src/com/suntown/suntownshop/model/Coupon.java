package com.suntown.suntownshop.model;

/**
 * 优惠券类
 *
 * @author Ken
 * @version 2015年3月10日 上午10:02:55
 *
 */
public class Coupon {
	private String id;
	private double denomination;
	private String addDate;
	private String endDate;
	private String useDate;
	private int couponType;
	private String typeName;
	private int type;
	public final static int TYPE_FROM_REGISTER = 0;
	public final static int TYPE_NULL = -1;
	public Coupon(String id, double denomination, String addDate,
			String endDate,String useDate, int type,int couponType,String typeName) {
		this.id = id;
		this.denomination = denomination;
		this.addDate = addDate;
		this.endDate = endDate;
		this.useDate = useDate;
		this.type = type;
		this.couponType = couponType;
		this.typeName = typeName;
	}
	
	public int getCouponType() {
		return couponType;
	}
	
	
	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public void setCouponType(int couponType) {
		this.couponType = couponType;
	}

	public String getUseDate() {
		return useDate;
	}

	public void setUseDate(String useDate) {
		this.useDate = useDate;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public double getDenomination() {
		return denomination;
	}

	public void setDenomination(double denomination) {
		this.denomination = denomination;
	}

	public String getAddDate() {
		return addDate;
	}

	public void setAddDate(String addDate) {
		this.addDate = addDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

}
