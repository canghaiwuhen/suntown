package com.suntown.suntownshop.model;

public class Evaluate {
	private int id;
	private String memid;
	private String nickname;
	private String avatar;
	private int rate;
	private String evaText;
	private String evaDate;
	
	public Evaluate(int id,String memid,String nickname,String avatar,int rate,String evaText,String evaDate){
		this.id = id;
		this.memid = memid;
		this.nickname = nickname;
		this.avatar = avatar;
		this.rate = rate;
		this.evaText  = evaText;
		this.evaDate = evaDate;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMemid() {
		return memid;
	}

	public void setMemid(String memid) {
		this.memid = memid;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}

	public int getRate() {
		return rate;
	}

	public void setRate(int rate) {
		this.rate = rate;
	}

	public String getEvaText() {
		return evaText;
	}

	public void setEvaText(String evaText) {
		this.evaText = evaText;
	}

	public String getEvaDate() {
		return evaDate;
	}

	public void setEvaDate(String evaDate) {
		this.evaDate = evaDate;
	}
	
	
}
